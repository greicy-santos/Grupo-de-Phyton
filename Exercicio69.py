idade = 18

print('A idade é menor do que 20? \n' +str(idade < 20))

print('A idade é maior que 50? \n' +str(idade > 50))

print('A idade é maior ou igual a 18? \n' +str(idade >= 18))

print('A idade é menor ou igual a 15? \n' +str(idade <= 15))


#esses sao operadores de comparaçao que dependendo do resultado da comparaçao ele retorna true or false, no caso do < ele compara tudo o que for
#abaixo do valor comparado, nao incluindo o valor, o mesmo ocorre com o > a diferença é que ele compara tudo o que for maior que ele, nao incluindo
#ele mesmo, ja o >= e o <= ele faz a comparaçao utilizando o valor em questao, ou seja, no caso do <= 20 ele compara tudo o que for menor que 20 incluindo
#o 20, ja no >= 18 ele compara tudo o que for maior e que for igual a 18.