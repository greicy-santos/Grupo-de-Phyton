numeros = [1,2,3,4,5,6,7]

print('O menor número é: ' +str(min(numeros)))

print('\nO maior número é: ' +str(max(numeros)))

print('\nA soma dos números é: ' +str(sum(numeros)) + '\n')

#A função min exibe o menor número da lista, a função max mostra o maior número e a funçao sum faz a soma de todos os elementos da lista

