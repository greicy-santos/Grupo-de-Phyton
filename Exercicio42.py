numeros = []

for valor in range(1,11):
  calculo = value**2
  numeros.append(calculo)

print(numeros)

#Nesse caso é declarado uma lista vazia, o método range vai de 1 a 10 e vai armazenando esses valores dentro da variavel valor 
#a variavel calculo vai armazenar a conta do numero que está armazenado na variavel valor naquele momento e é feito o cálculo daquele valor ao quadrado
#na linha 5 é adicionado o resultado do cálculo na lista e ao final é impresso todos os valores da lista.